import Anthropic from "@anthropic-ai/sdk";
import config from "../utils/config.js";
import logger from "../utils/logger.js";
import { readMarkdown, readJson } from "../utils/corpus.js";

let client = null;

function getClient() {
  if (!client && config.anthropic.apiKey && config.anthropic.apiKey !== "placeholder") {
    client = new Anthropic({ apiKey: config.anthropic.apiKey });
  }
  return client;
}

/**
 * Generate a radio show from assembled runtime context.
 *
 * @param {object} runtimeContext — assembled by context.js:
 *   { weather, schedule, preferences, history, now }
 * @returns {object} { ok, data: { showTitle, mood, reason, playlist, djScripts } }
 */
export async function generateShow(runtimeContext) {
  const claude = getClient();
  if (!claude) {
    logger.warn("Claude API not configured, returning demo show");
    return { ok: true, data: demoTrack(), mock: true };
  }

  const systemPrompt = readMarkdown("templates/system-prompt.md");
  const now = runtimeContext.now || new Date().toISOString();

  const userMessage = [
    `现在是 ${now}`,
    "",
    "## 用户情境数据",
    "```json",
    JSON.stringify(runtimeContext, null, 2),
    "```",
    "",
    "请根据以上情境数据，为当前时刻策划一期音乐节目。",
  ].join("\n");

  try {
    logger.info({ model: config.anthropic.model }, "Calling Claude...");

    const msg = await claude.messages.create({
      model: config.anthropic.model,
      max_tokens: 2048,
      temperature: 0.8,
      system: systemPrompt,
      messages: [{ role: "user", content: userMessage }],
    });

    const text = extractText(msg);
    const show = parseShowJson(text);

    if (!show) {
      logger.error({ text: text.substring(0, 300) }, "Failed to parse Claude response");
      return { ok: false, error: "Failed to parse show JSON", data: demoTrack() };
    }

    logger.info({ title: show.showTitle, tracks: show.playlist?.length }, "Show generated by Claude");
    return { ok: true, data: show };
  } catch (err) {
    logger.error({ error: err.message }, "Claude API call failed");
    return { ok: false, error: err.message, data: demoTrack() };
  }
}

// ── Response Parsing ──────────────────────────────────────────

function extractText(message) {
  const block = message.content.find((b) => b.type === "text");
  return block?.text || "";
}

function parseShowJson(text) {
  // Try raw parse first
  try { return JSON.parse(text); } catch { /* continue */ }

  // Try extracting from ```json code block
  const fence = text.match(/```(?:json)?\s*([\s\S]*?)```/);
  if (fence) {
    try { return JSON.parse(fence[1].trim()); } catch { /* continue */ }
  }

  // Try finding a JSON object in the text
  const objMatch = text.match(/\{[\s\S]*\}/);
  if (objMatch) {
    try { return JSON.parse(objMatch[0]); } catch { /* continue */ }
  }

  return null;
}

